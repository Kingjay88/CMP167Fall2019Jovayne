import java.util.Scanner;

public class factorial {

	public static void main(String[] args) {
		Scanner input = new Scanner(System.in);
		
		int i, ninja = 1;
		int jay;
		
		
		System.out.println("pick a number");
		jay = input.nextInt();
		
		

		for (i = 1; i <= jay; i++) {
			ninja = ninja * i; }
		
		System.out.println(ninja);
		
		
		
		
		
		
		
	
		
		
		
		

	}

}
